"""Q7. Check if a number is palindrome.

Approach:
- Reverse number
- Compare with original
"""

#write answer here
n = int(input("Enter a number: "))
original = n
reverse = 0

while n != 0:
    digit = n % 10
    reverse = reverse * 10 + digit
    n = n // 10

if original == reverse:
    print("Palindrome number")
else:
    print("Not a palindrome number")

